<h1>Frequently Asked Questions</h1>

<h2>How can I share my learning with my manager?</h2>
<ul class="mb-3">
<li>You can save a copy of your profile page, which will show which steps you’ve taken on which pathways. Share your saved page with your manager in a one:one meeting and discuss your plans.
<li>You can enter Learning Agent pathways as goals in your MyPerformance Plan. Discuss the goal with your manager, and if you like, set target dates for completing steps and pathways.
</ul>


<h2>How can I track my learning? Do my completed pathways show in my Learning Record</h2>
<ul class="mb-3">
<li>Claim activities as you complete them by clicking the claim button. This record of what videos you’ve watched, readings you’ve read, and courses you’ve taken and so on will not be reported out anywhere, but is just for you. You can view a list of previously claimed items at any time on your profile page.
</ul>


<h2>Will the Learning Agent remember what I’ve done?</h2>
<ul class="mb-3">
<li>Once you log into the Learning Agent with your IDIR for the first time, a profile is created for you. Learning Agent will retain your chosen pathways and claimed items between visits.
</ul>


<h2>What happens when I follow a pathway?</h2>
<ul class="mb-3">
<li>The pathway will show that you are following, and will include it on a list of pathways you’re on when you visit your profile page. The rest is up to you—engage as much or as little as you see fit with the resources within each step.
</ul>


<h2>How do I complete a pathway?</h2>
<ul class="mb-3">
<li>Once you claim all the required activities in all of the steps along the pathway, your pathway will be marked complete.
</ul>


<h2>What happens when I complete a pathway?</h2>
<ul class="mb-3">
<li>It will show complete on your profile page. You will always have access to your pathway. Return from time to time to engage with any new steps or resources as they are added.
</ul>


<h2>What are “archived” items in a pathway?</h2>
<ul class="mb-3">
<li>Curators will always be keeping the pathways up-to-date. Occasionally an item may be removed from the recommendations. If an item that you claimed is no longer part of the active pathway, you can check a list of archived items to access it.
</ul>


<h2>What are the four different formats?</h2>
<ul class="mb-3">
<li>Items are categorized as watch (video), listen (audio recordings, including podcasts), read (readings of all kinds, including online articles and book recommendations), and participate (including face to face and online classes you take with others, or self-reflective activities or action items that require some output from you). Using the tags, you can quickly scan for the type of activity you have the capacity to engage with right now.
</ul>



